var countDownDate = new Date("Aug 08, 2020 00:00:00").getTime();
var x = setInterval(function() {
  var now = new Date().getTime();
  var distance = countDownDate - now;
  ///////var months = Math.floor(distance / (1000 * 60 * 60 * 24 * 7));
//   var weeks = Math.floor(distance / (1000 * 60 * 60 * 24 * 7));
  var days = Math.floor(distance / (1000 * 60 * 60 * 24));
  var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
  var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
  var seconds = Math.floor((distance % (1000 * 60)) / 1000);
    
  document.getElementById("count_days").innerHTML =  // weeks + "saptamani <br>" +
													days + " zile <br>" 
													+ hours + " ore <br>"
													+ minutes + " minute <br>" 
													+ seconds + " secunde <br>";
    
  if (distance < 0) {
    clearInterval(x);
    document.getElementById("count_days").innerHTML = "Speram ca te-ai distrat alaturi de noi!";
  }
}, 1000);
 
